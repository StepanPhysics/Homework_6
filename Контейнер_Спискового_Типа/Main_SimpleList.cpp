#include <iostream>
#include "SimpleList.h"

void print_list(SimpleList<int>& list) {
    for (size_t i = 0; i < list.size(); ++i) {
        std::cout << list[i];
        if (i < list.size() - 1) {
            std::cout << ", ";
        }
    }
    std::cout << std::endl;
}

int main() {
    SimpleList<int> list;

    // 1. Добавление в контейнер десяти элементов (0, 1 … 9)
    for (int i = 0; i < 10; ++i) {
        list.push_back(i);
    }

    // 2. Вывод содержимого контейнера на экран
    std::cout << "Содержимое контейнера: ";
    print_list(list);

    // 3. Вывод размера контейнера на экран
    std::cout << "Размер контейнера: " << list.size() << std::endl;

    // 4. Удаление третьего (по счёту), пятого и седьмого элементов
    list.remove(2); // Удаляем элемент с индексом 2 (3-й по счёту)
    list.remove(3); // Учитывая уменьшение индексов, это теперь 5-й по счёту
    list.remove(4); // Учитывая уменьшение индексов, это теперь 7-й по счёту

    // 5. Вывод содержимого контейнера на экран
    std::cout << "После удаления: ";
    print_list(list);

    // 6. Добавление элемента 10 в начало контейнера
    list.insert(0, 10);

    // 7. Вывод содержимого контейнера на экран
    std::cout << "После добавления 10 в начало: ";
    print_list(list);

    // 8. Добавление элемента 20 в середину контейнера
    list.insert(list.size() / 2, 20);

    // 9. Вывод содержимого контейнера на экран
    std::cout << "После добавления 20 в середину: ";
    print_list(list);

    // 10. Добавление элемента 30 в конец контейнера
    list.push_back(30);

    // 11. Вывод содержимого контейнера на экран
    std::cout << "После добавления 30 в конец: ";
    print_list(list);

    return 0;
}